package cls;

public class LengthConversion {
		
	public float centimeterToMeter(float length) {		
		return length / 100;
		
	}
	
	public float meterToCentimeter(float length) {		
		return length * 100;
		
	}
	
	public float centimeterToKilometer(float length) {		
		return length / 100_000;
		
	}
	
	public float KilometerToCentimeter(float length) {
		return length * 100_000;
		
	}
	
}
